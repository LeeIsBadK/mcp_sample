def _next_5pm_starttime(bkk_now: datetime) -> str:
    """Return 'YYYY-MM-DDT17:00:00' in Asia/Bangkok."""
    target = bkk_now if bkk_now.hour < 17 else (bkk_now + timedelta(days=1))
    return target.strftime("%Y-%m-%dT17:00:00")
